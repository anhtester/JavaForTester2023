package Bai15_TinhDaHinh_TruuTuong.TinhDaHinh;

public class Calculator {

    //Thể hiện tính đa hình
    public int tinhTong(int number1, int number2) {
        return number1 + number2;
    }

    public int tinhTong(int number1, int number2, int number3) {
        return number1 + number2 + number3;
    }

    public int phepNhan(int number1, int number2) {
        return number1 * number2;
    }

    public double phepNhan(double number1, double number2) {
        return number1 * number2;
    }

    public double phepNhan(double number1, double number2, double number3) {
        return number1 * number2 * number3;
    }

    public double phepNhan(double number1, double number2, double number3, double number4) {
        return number1 * number2 * number3 * number4;
    }

}
